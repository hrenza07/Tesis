    private void LimpiarJTable(){
        tablaDepart=(DefaultTableModel)tablaDepartamento.getModel();
        int a =tablaDepart.getRowCount()-1;
        for(int i=a;i>=0;i--){ 
            tablaDepart.removeRow(i);
        }
    }

    

