public class funcionesCargo {
    
    private int id;
    private String tarea;
    private cargo c;
    private int idx;

    public funcionesCargo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTarea() {
        return tarea;
    }

    public void setTarea(String tarea) {
        this.tarea = tarea;
    }

    public cargo getC() {
        return c;
    }

    public void setC(cargo c) {
        this.c = c;
    }

    public int getIdx() {
        return idx;
    }

    public void setIdx(int idx) {
        this.idx = idx;
    }
    
    
    
}
